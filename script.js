const quizForm = document.getElementById('quizForm');
const steps = [...document.querySelectorAll('.quiz-step')];
const nextStep = document.getElementById('nextStep');
const prevStep = document.getElementById('prevStep');
const submitQuiz = document.getElementById('submitQuiz');
const quizProgress = document.getElementById('quizProgress');
let currentStep = 0;

function showStep(index){
  steps.forEach((s, i) => s.classList.toggle('active', i === index));
  if(quizProgress) quizProgress.style.width = `${((index + 1) / steps.length) * 100}%`;
  if(prevStep) prevStep.style.display = index === 0 ? 'none' : 'inline-flex';
  if(nextStep) nextStep.style.display = index === steps.length - 1 ? 'none' : 'inline-flex';
  if(submitQuiz) submitQuiz.style.display = index === steps.length - 1 ? 'inline-flex' : 'none';
}
showStep(currentStep);

function validateCurrentStep(){
  const active = steps[currentStep];
  const required = active?.querySelectorAll('[required]') || [];
  for(const field of required){
    if(!field.value.trim()){
      field.focus();
      return false;
    }
  }
  return true;
}
nextStep?.addEventListener('click', () => {
  if(!validateCurrentStep()) return;
  currentStep = Math.min(currentStep + 1, steps.length - 1);
  showStep(currentStep);
});
prevStep?.addEventListener('click', () => {
  currentStep = Math.max(currentStep - 1, 0);
  showStep(currentStep);
});

quizForm?.addEventListener('submit', (event) => {
  event.preventDefault();
  if(!validateCurrentStep()) return;

  const data = new FormData(quizForm);
  const text =
    `${t('wa.hello')}%0A` +
    `${t('wa.name')}: ${encodeURIComponent(data.get('name') || t('wa.notSet'))}%0A` +
    `${t('wa.service')}: ${encodeURIComponent(data.get('service') || t('wa.notSet'))}%0A` +
    `${t('wa.size')}: ${encodeURIComponent(data.get('size') || t('wa.notSet'))}%0A` +
    `${t('wa.mount')}: ${encodeURIComponent(data.get('mount') || t('wa.notSet'))}%0A` +
    `${t('wa.layout')}: ${encodeURIComponent(data.get('layout') || t('wa.notSet'))}%0A` +
    `${t('wa.comment')}: ${encodeURIComponent(data.get('message') || '')}`;

  window.open(`https://wa.me/77776557272?text=${text}`, '_blank');
  quizForm.reset();
  currentStep = 0;
  showStep(currentStep);
});
